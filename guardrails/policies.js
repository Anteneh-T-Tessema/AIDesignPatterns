export const REGEX_PATTERNS = {
  SSN: /\b\d{3}-\d{2}-\d{4}\b/g,
  API_KEY: /\b(API_KEY_SECRET_[a-zA-Z0-9]{8,16})\b/gi,
  PASSWORD_MATCH: /\bpassword\s*[:=]\s*[^\s]{6,}\b/gi
};

export const FALLBACK_MESSAGES = {
  INPUT_BLOCKED: "❌ Access Denied: Your request was blocked by the input safety guardrails as it violates corporate compliance policies.",
  OUTPUT_BLOCKED: "🔒 Security Alert: The response generated by the AI was blocked and sanitized. It contained sensitive corporate secrets or PII (API Keys/SSN)."
};

/**
 * Builds the system prompt for the Input Guardrail Agent.
 */
export function buildInputGuardrailPrompt(userInput) {
  const policyBullets = INPUT_POLICIES.map(p => ` - **${p.id}** [${p.name}]: ${p.directive}`).join("\n");
  
  return `You are the AI Content Policy Enforcer (Safety Guardrail).
Your task is to pre-screen user prompts before they reach the primary database agent.
You must evaluate the user's prompt against these strict corporate compliance policies:

${policyBullets}

---
Evaluate the following User Prompt:
"${userInput}"

Output your response strictly in the following JSON format:
{
  "compliance_status": "compliant" or "non-compliant",
  "evaluation_summary": "Short explanation of compliance status or identifying which specific policy was triggered.",
  "triggered_policies": ["POLICY_001_JAILBREAK", "etc"] (list triggered policy IDs, or empty array if compliant)
}`;
}

/**
 * Builds the system prompt for the Output Guardrail Agent.
 */
export function buildOutputGuardrailPrompt(generatedOutput) {
  const policyBullets = OUTPUT_POLICIES.map(p => ` - **${p.id}** [${p.name}]: ${p.directive}`).join("\n");

  return `You are the AI Output Security Inspector.
Your task is to scan the AI's generated response before it is displayed to the user.
Evaluate the response against these output compliance policies:

${policyBullets}

---
Scan the following AI Response:
"${generatedOutput}"

Output your response strictly in the following JSON format:
{
  "compliance_status": "compliant" or "non-compliant",
  "evaluation_summary": "Short explanation of compliance status or details of any violations (toxicity, competitor slandering, private data leaks).",
  "triggered_policies": ["POLICY_005_PII_LEAK", "etc"] (list triggered policy IDs, or empty array if compliant)
}`;
}
